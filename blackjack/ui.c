void dibujar_mensaje(const char *texto, int segundos, int color)
{
    printf("\033[%d;%dH", 18, 30); // Mueve el cursor a la posiciГіn (17, 30)

    // Establece el color del texto
    printf("\033[0;%dm", color);

    printf("%s", texto); // Imprime el texto en la posiciГіn especificada

    if (segundos > 1)
    {
        Sleep(segundos * 1000); // Espera el tiempo especificado
        printf("\033[%d;%dH                                                 \n", 18, 30); // Borra el mensaje
    }
    if (segundos == -1)
    {
        printf("%s", texto);
    }
}

