void dibujar_barajarmazo(int x, int y)
{
    int i;
    for (i = 0; i < 5; i++)
    {
        // Borra el mazo dibujado anteriormente
        borrar_mazo(x, y);

        // Dibuja el mazo en la nueva posiciГіn
        dibujar_mazo(x + i, y);

        // Introduce un retraso para que se vea la animaciГіn
        // El valor dentro de usleep es el tiempo en microsegundos
        // AquГӯ se espera 100,000 microsegundos, es decir, 0.1 segundo
        usleep(100000); // Cambiar a Sleep(100) si se estГЎ utilizando Windows
    }
}




