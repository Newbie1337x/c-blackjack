void borrar_mazo(int x, int y)
{
    // Suponiendo que el mazo ocupa un ГЎrea rectangular en la pantalla
    // con un tamaГұo especГӯfico (por ejemplo, 5 filas y 9 columnas)

    // Itera sobre cada posiciГіn ocupada por el mazo en la pantalla
    for (int i = 0; i < 5; i++)
    {
        for (int j = 0; j < 9; j++)
        {
            printf("\033[%d;%dH", y + i, x + j ); // Mueve el cursor a la posiciГіn
            printf(" "); // Borra el carГЎcter en esa posiciГіn
        }
    }
}



